    public void connect(){
        appendMessage(" Connecting...", "Status", Color.GREEN.darker(), Color.GREEN.darker());
        try {
            socket = new Socket(host, port);
            dos = new DataOutputStream(socket.getOutputStream());
            // gửi username đang kết nối
            dos.writeUTF("CMD_JOIN "+ username);
            appendMessage(" Connected", "Status", Color.GREEN.darker(), Color.GREEN.darker());
            appendMessage(" send message now!", "Status", Color.GREEN.darker(), Color.GREEN.darker());
            
            // Start client thread
            new Thread(new ClientThread(socket, this)).start();
            jButton1.setEnabled(true);
            // Checks if already connected
            isConnected = true;
            
        }
        catch(IOException e) {
            isConnected = false;
            JOptionPane.showMessageDialog(this, "Cannot connect to server!","Connection failed!",JOptionPane.ERROR_MESSAGE);
            appendMessage("[IOException]: "+ e.getMessage(), "Error", Color.RED.darker(), Color.RED.darker());
        }
    }
    
